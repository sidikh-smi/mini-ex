#include "../include/minishell.h"

t_token	*init_token(char *value, int type)
{
	t_token	*token;

	token = s_malloc(sizeof(t_token));
	if (!token)
		return (NULL);
	token->value = value;
	token->type = type;
	return (token);
}

t_token	*get_token(t_lexer *lexer)
{
	t_token	*token;
	char	*s;
	bool	in_double;
	bool	in_single;

	in_double = 0;
	in_single = 0;
	s = NULL;
	skip_spaces(lexer);
	while ((lexer->c != '\0') && (!istoken(lexer->c)
			|| in_double || in_single))
	{
		if (!in_single && lexer->c == '\"')
			in_double = !in_double;
		if (!in_double && lexer->c == '\'')
			in_single = !in_single;
		s = append_to_str(s, lexer->c);
		lexer_next(lexer);
	}
	if (s)
		token = init_token (s, TOKEN_STRING);
	else
		token = _switch(lexer);
	return (token);
}

int	redirect(t_lexer *lexer)
{
	if ((lexer)->c == '>')
	{
		lexer_next (lexer);
		return (1);
	}
	return (0);
}

int	istoken(char c)
{
	if (c == '>' || c == '<' || c == '|' || c == '\0' || c == ' ')
		return (1);
	return (0);
}

t_token	*_switch(t_lexer *lexer)
{
	if (lexer->c == '>')
	{
		lexer_next(lexer);
		if (redirect(lexer))
			return (init_token(">>", TOKEN_APPEND));
		return (init_token(">", TOKEN_REDIRECT));
	}
	if (lexer->c == '<')
	{
		lexer_next(lexer);
		if (lexer->c == '<')
		{
			lexer_next(lexer);
			return (init_token("<<", TOKEN_HEREDOC));
		}
		return (init_token("<", TOKEN_LREDIRECT));
	}
	if (lexer->c == '|')
	{
		lexer_next(lexer);
		return (init_token("|", TOKEN_PIPE));
	}
	else
		return (init_token("EOF", TOKEN_EOF));
}
