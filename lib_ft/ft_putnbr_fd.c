#include "libft.h"

void	ft_putnbr_fd(int n, int fd)
{
	char	t;

	if (n < 0)
	{
		write(fd, "-", 1);
		if (n == -2147483648)
		{
			write(fd, "2147483648", 10);
			return ;
		}
		else
			n = n * -1;
	}
	if (n > 9)
		ft_putnbr_fd(n / 10, fd);
	t = (n % 10) + '0';
	write(fd, &t, 1);
}
